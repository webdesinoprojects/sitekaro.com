export function parseLocations() {
  const lines = rawLocationsData.split('\n').filter(line => line.trim() !== '');
  const locations = [];

  for (const line of lines) {
    // Remove date (DD/MM/YYYY) and day (Monday-Sunday)
    let cleanedLine = line.replace(/\d{2}\/\d{2}\/\d{4}/g, '').replace(/(Monday|Tuesday|Wednesday|Thursday|Friday|Saturday|Sunday)/g, '').trim();
    
    // Extract URL (starts with http or https)
    const urlMatch = cleanedLine.match(/(https?:\/\/[^\s]+)/);
    
    if (urlMatch) {
      const url = urlMatch[1];
      // Location is everything before the URL
      const locationName = cleanedLine.substring(0, cleanedLine.indexOf(url)).trim();
      
      // Extract slug from URL
      // Remove trailing slash if present
      const cleanUrl = url.endsWith('/') ? url.slice(0, -1) : url;
      const slug = cleanUrl.split('/').pop() || '';
      
      // Generate Title from Slug
      // e.g. best-web-developer-in-new-delhi -> Best Web Developer In New Delhi
      const title = slug
        .split('-')
        .map(word => word.charAt(0).toUpperCase() + word.slice(1))
        .join(' ');

      if (slug && locationName) {
        locations.push({
          location: locationName,
          slug: slug,
          title: title
        });
      }
    } else {
        // Handle the last case: "Depot Station Best Web Development Company in Depot Station"
        // It doesn't have a URL, but looks like "Location Title"
        // Or maybe the user missed the URL.
        // The last line is: "Depot Station 	Best Web Development Company in Depot Station"
        // It seems to be Location + Title.
        // Let's try to infer slug.
        // "Best Web Development Company in Depot Station" -> best-web-development-company-in-depot-station
        
        // Split by tab or multiple spaces
        const parts = cleanedLine.split(/\s{2,}/);
        if (parts.length >= 2) {
            const locationName = parts[0].trim();
            const title = parts[1].trim();
            const slug = title.toLowerCase().replace(/ /g, '-');
             locations.push({
                location: locationName,
                slug: slug,
                title: title
            });
        }
    }
  }
  return locations;
}
